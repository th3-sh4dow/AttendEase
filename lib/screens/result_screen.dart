import 'package:flutter/material.dart';
import 'package:flutter/services.dart';
import 'package:share_plus/share_plus.dart';
import '../models/student.dart';
import '../services/pdf_service.dart';
import 'home_screen.dart';

class ResultScreen extends StatefulWidget {
  final List<Student> students;

  const ResultScreen({super.key, required this.students});

  @override
  State<ResultScreen> createState() => _ResultScreenState();
}

class _ResultScreenState extends State<ResultScreen> {
  bool _isGeneratingPdf = false;

  String _generateTextReport() {
    final presentStudents = widget.students.where((s) => s.isPresent).toList();
    final absentStudents = widget.students.where((s) => !s.isPresent).toList();
    
    final buffer = StringBuffer();
    buffer.writeln('ATTENDANCE REPORT');
    buffer.writeln('Generated by AttendEase App');
    buffer.writeln('Date: ${DateTime.now().toString().split(' ')[0]}');
    buffer.writeln('Time: ${TimeOfDay.now().format(context)}');
    buffer.writeln('=' * 40);
    buffer.writeln();
    
    buffer.writeln('SUMMARY:');
    buffer.writeln('Total Students: ${widget.students.length}');
    buffer.writeln('Present: ${presentStudents.length}');
    buffer.writeln('Absent: ${absentStudents.length}');
    buffer.writeln();
    
    if (presentStudents.isNotEmpty) {
      buffer.writeln('PRESENT STUDENTS:');
      buffer.writeln('-' * 20);
      for (var student in presentStudents) {
        buffer.writeln('${student.rollNumber} - ${student.name}');
      }
      buffer.writeln();
    }
    
    if (absentStudents.isNotEmpty) {
      buffer.writeln('ABSENT STUDENTS:');
      buffer.writeln('-' * 20);
      for (var student in absentStudents) {
        buffer.writeln('${student.rollNumber} - ${student.name}');
      }
      buffer.writeln();
    }
    
    buffer.writeln('=' * 40);
    buffer.writeln('Generated by AttendEase');
    buffer.writeln('Developer: Bicky Muduli');
    buffer.writeln('Email: bickymuduli289@gmail.com');
    buffer.writeln('GitHub: https://github.com/th3-sh4dow');
    buffer.writeln('=' * 40);
    
    return buffer.toString();
  }

  Future<void> _generatePdfReport() async {
    setState(() {
      _isGeneratingPdf = true;
    });

    try {
      final filePath = await PdfService.generateAttendanceReport(
        widget.students,
        'Attendance Report',
      );
      
      if (mounted) {
        _showPdfGeneratedDialog(filePath);
      }
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Error generating PDF: $e'),
            backgroundColor: Colors.red,
          ),
        );
      }
    } finally {
      if (mounted) {
        setState(() {
          _isGeneratingPdf = false;
        });
      }
    }
  }

  void _showPdfGeneratedDialog(String filePath) {
    showDialog(
      context: context,
      builder: (context) => AlertDialog(
        title: const Row(
          children: [
            Icon(Icons.check_circle, color: Colors.green),
            SizedBox(width: 8),
            Text('PDF Generated!'),
          ],
        ),
        content: const Text(
          'Your attendance report has been generated successfully. What would you like to do?',
        ),
        actions: [
          TextButton(
            onPressed: () => Navigator.pop(context),
            child: const Text('Close'),
          ),
          ElevatedButton.icon(
            onPressed: () {
              Navigator.pop(context);
              _sharePdf(filePath);
            },
            icon: const Icon(Icons.share),
            label: const Text('Share PDF'),
          ),
        ],
      ),
    );
  }

  Future<void> _sharePdf(String filePath) async {
    try {
      final presentCount = widget.students.where((s) => s.isPresent).length;
      final totalCount = widget.students.length;
      
      await Share.shareXFiles(
        [XFile(filePath)],
        text: 'Attendance Report - Present: $presentCount/$totalCount students\n'
              'Generated by AttendEase App',
        subject: 'Attendance Report - ${DateTime.now().toString().split(' ')[0]}',
      );
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Error sharing PDF: $e'),
            backgroundColor: Colors.red,
          ),
        );
      }
    }
  }

  void _copyToClipboard() {
    Clipboard.setData(ClipboardData(text: _generateTextReport()));
    ScaffoldMessenger.of(context).showSnackBar(
      const SnackBar(content: Text('Report copied to clipboard')),
    );
  }

  Future<void> _shareTextReport() async {
    try {
      final textReport = _generateTextReport();
      final presentCount = widget.students.where((s) => s.isPresent).length;
      final totalCount = widget.students.length;
      
      await Share.share(
        textReport,
        subject: 'Attendance Report - Present: $presentCount/$totalCount students - ${DateTime.now().toString().split(' ')[0]}',
      );
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Error sharing report: $e'),
            backgroundColor: Colors.red,
          ),
        );
      }
    }
  }

  void _goHome() {
    Navigator.pushAndRemoveUntil(
      context,
      MaterialPageRoute(builder: (context) => const HomeScreen()),
      (route) => false,
    );
  }

  @override
  Widget build(BuildContext context) {
    final presentStudents = widget.students.where((s) => s.isPresent).toList();
    final absentStudents = widget.students.where((s) => !s.isPresent).toList();

    return Scaffold(
      appBar: AppBar(
        title: const Text('Attendance Results'),
        backgroundColor: Theme.of(context).colorScheme.inversePrimary,
        automaticallyImplyLeading: false,
        actions: [
          IconButton(
            icon: const Icon(Icons.home),
            onPressed: _goHome,
          ),
        ],
      ),
      body: SingleChildScrollView(
        padding: const EdgeInsets.all(16),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            // Summary Card
            Card(
              child: Padding(
                padding: const EdgeInsets.all(16),
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Text(
                      'Summary',
                      style: Theme.of(context).textTheme.headlineSmall,
                    ),
                    const SizedBox(height: 12),
                    Row(
                      mainAxisAlignment: MainAxisAlignment.spaceAround,
                      children: [
                        _buildSummaryItem(
                          'Total',
                          widget.students.length.toString(),
                          Colors.blue,
                        ),
                        _buildSummaryItem(
                          'Present',
                          presentStudents.length.toString(),
                          Colors.green,
                        ),
                        _buildSummaryItem(
                          'Absent',
                          absentStudents.length.toString(),
                          Colors.red,
                        ),
                      ],
                    ),
                  ],
                ),
              ),
            ),
            const SizedBox(height: 16),

            // Action Buttons
            Column(
              children: [
                Row(
                  children: [
                    Expanded(
                      child: ElevatedButton.icon(
                        onPressed: _copyToClipboard,
                        icon: const Icon(Icons.copy),
                        label: const Text('Copy Text'),
                      ),
                    ),
                    const SizedBox(width: 12),
                    Expanded(
                      child: ElevatedButton.icon(
                        onPressed: _isGeneratingPdf ? null : _generatePdfReport,
                        icon: _isGeneratingPdf
                            ? const SizedBox(
                                width: 16,
                                height: 16,
                                child: CircularProgressIndicator(strokeWidth: 2),
                              )
                            : const Icon(Icons.picture_as_pdf),
                        label: const Text('Generate PDF'),
                      ),
                    ),
                  ],
                ),
                const SizedBox(height: 12),
                SizedBox(
                  width: double.infinity,
                  child: ElevatedButton.icon(
                    onPressed: () => _shareTextReport(),
                    icon: const Icon(Icons.share),
                    label: const Text('Share Text Report'),
                    style: ElevatedButton.styleFrom(
                      backgroundColor: Colors.green,
                      foregroundColor: Colors.white,
                    ),
                  ),
                ),
              ],
            ),
            const SizedBox(height: 16),

            // Present Students
            if (presentStudents.isNotEmpty) ...[
              Card(
                child: Padding(
                  padding: const EdgeInsets.all(16),
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Row(
                        children: [
                          const Icon(Icons.check_circle, color: Colors.green),
                          const SizedBox(width: 8),
                          Text(
                            'Present Students (${presentStudents.length})',
                            style: Theme.of(context).textTheme.titleLarge,
                          ),
                        ],
                      ),
                      const SizedBox(height: 12),
                      ...presentStudents.map((student) => Padding(
                        padding: const EdgeInsets.symmetric(vertical: 4),
                        child: Row(
                          children: [
                            Expanded(
                              flex: 2,
                              child: Text(
                                student.rollNumber,
                                style: const TextStyle(fontWeight: FontWeight.w500),
                              ),
                            ),
                            Expanded(
                              flex: 3,
                              child: Text(student.name),
                            ),
                          ],
                        ),
                      )),
                    ],
                  ),
                ),
              ),
              const SizedBox(height: 16),
            ],

            // Absent Students
            if (absentStudents.isNotEmpty) ...[
              Card(
                child: Padding(
                  padding: const EdgeInsets.all(16),
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Row(
                        children: [
                          const Icon(Icons.cancel, color: Colors.red),
                          const SizedBox(width: 8),
                          Text(
                            'Absent Students (${absentStudents.length})',
                            style: Theme.of(context).textTheme.titleLarge,
                          ),
                        ],
                      ),
                      const SizedBox(height: 12),
                      ...absentStudents.map((student) => Padding(
                        padding: const EdgeInsets.symmetric(vertical: 4),
                        child: Row(
                          children: [
                            Expanded(
                              flex: 2,
                              child: Text(
                                student.rollNumber,
                                style: const TextStyle(fontWeight: FontWeight.w500),
                              ),
                            ),
                            Expanded(
                              flex: 3,
                              child: Text(student.name),
                            ),
                          ],
                        ),
                      )),
                    ],
                  ),
                ),
              ),
            ],

            const SizedBox(height: 24),
            SizedBox(
              width: double.infinity,
              child: ElevatedButton.icon(
                onPressed: _goHome,
                icon: const Icon(Icons.home),
                label: const Text('Back to Home'),
                style: ElevatedButton.styleFrom(
                  padding: const EdgeInsets.all(16),
                  textStyle: const TextStyle(fontSize: 16),
                ),
              ),
            ),
          ],
        ),
      ),
    );
  }

  Widget _buildSummaryItem(String label, String value, Color color) {
    return Column(
      children: [
        Container(
          padding: const EdgeInsets.all(12),
          decoration: BoxDecoration(
            color: color.withValues(alpha: 0.1),
            borderRadius: BorderRadius.circular(8),
          ),
          child: Text(
            value,
            style: TextStyle(
              fontSize: 24,
              fontWeight: FontWeight.bold,
              color: color,
            ),
          ),
        ),
        const SizedBox(height: 4),
        Text(
          label,
          style: const TextStyle(
            fontSize: 12,
            fontWeight: FontWeight.w500,
          ),
        ),
      ],
    );
  }
}